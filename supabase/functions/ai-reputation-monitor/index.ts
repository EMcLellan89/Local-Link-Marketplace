import "jsr:@supabase/functions-js/edge-runtime.d.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Methods": "GET, POST, PUT, DELETE, OPTIONS",
  "Access-Control-Allow-Headers": "Content-Type, Authorization, X-Client-Info, Apikey",
};

Deno.serve(async (req: Request) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { status: 200, headers: corsHeaders });
  }

  try {
    const { messages, userId } = await req.json();

    const systemPrompt = `You are an expert online reputation management consultant for service-based businesses. Help merchants monitor and protect their reputation by:

1. Setting up monitoring systems:
   - Platform selection (Google, Yelp, Facebook, Angi, BBB, etc)
   - Real-time alert configuration (SMS, email, push)
   - Review source aggregation
   - Sentiment analysis thresholds
   - Priority alert rules (3-star and below)

2. Response strategies:
   - Positive review response templates
   - Negative review damage control
   - Professional tone maintenance
   - Brand voice consistency
   - Response time targets

3. Reputation improvement:
   - Competitor benchmarking
   - Rating trend analysis
   - Review generation campaigns
   - Crisis management protocols
   - Reputation score tracking

Be proactive, protective, and emphasize that responding 10x faster prevents reputation damage. Provide monitoring and response frameworks.`;

    const openAIResponse = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Authorization": `Bearer ${Deno.env.get("OPENAI_API_KEY")}`
      },
      body: JSON.stringify({
        model: "gpt-4o-mini",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages
        ],
        temperature: 0.7,
        max_tokens: 1000
      })
    });

    if (!openAIResponse.ok) {
      throw new Error("OpenAI API error");
    }

    const data = await openAIResponse.json();
    const response = data.choices[0].message.content;

    return new Response(
      JSON.stringify({ response }),
      { headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  } catch (error) {
    console.error("Error:", error);
    return new Response(
      JSON.stringify({ error: "Failed to generate response" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});